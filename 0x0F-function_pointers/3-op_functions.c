#include "3-calc.h"

/**
 * op_add - This function returns the sum,
 * of two integers.
 * @a: The first int.
 * @b: The second int.
 *
 * Return: it returns the sum.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - This function returns the difference,
 * of two integers.
 * @a: The first integer.
 * @b: The second integer.
 *
 * Return: It returns the difference.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - This function returns the product of,
 * two integers.
 * @a: the first int arg.
 * @b: The second int arg.
 *
 * Return: The product is returned.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - This function returns the quotient of,
 * two int.
 * @a: The first int arg.
 * @b: The second int arg.
 *
 * Return: The Quotient is returned.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - This function returns the modulus of,
 * two int args.
 * @a: The first arg passed to the function.
 * @b: The second arg passed to the function.
 *
 * Return: The modulus is returned.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
